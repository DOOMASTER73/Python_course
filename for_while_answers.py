#Для того щоб точніше рахувати секунди використовують секунди Місісіпі. Ми хочемо щоб програма порахувала 10 секунд і кожну сукунду виводила наш лічильник
from datetime import time
# Початкове значення лічильника
counter = 0

# Максимальне значення, до якого ми хочемо дійти
max_value = 10

# Цикл while, який збільшує лічильник
while counter < max_value:
    time.sleep(1)
    print(f"{counter} Місісіпі")
    counter += 1

print("Цикл завершено.")

