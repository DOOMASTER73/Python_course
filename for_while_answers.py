# Початковий список з рядками

original_list = ["apple", "banana", "cherry", "date", "elderberry", "fig"]

# Створення нового списку з використанням циклу for
filtered_list = []

for word in original_list:
    if word.startswith("f"):
        filtered_list.append(word)

# Виведення нового списку
print(filtered_list)

